//program for demonstrate the Bitwise Operator
package org.tnsif.operators;

public class BitwiseOperatorDemo {

	public static void main(String[] args) {
		int x =56, y=5;
		System.out.println(x&y);
		System.out.println(x|y);
		System.out.println(x^y);
		System.out.println(x<<y);
		System.out.println(x>>y);


	}

}
